/** Look up all techniques for a given tactic ID */
export function getTechniquesByTactic(tacticId: string): MitreTechnique[] {
  return MITRE_TECHNIQUES.filter((t) => t.tacticIds.includes(tacticId))
}

/** Look up a single technique by ID */
export function getTechniqueById(id: string): MitreTechnique | undefined {
  return MITRE_TECHNIQUES.find((t) => t.id === id)
}
